-- user_spot.tags — лёгкая схема тэгов на сохранённое место.
--
-- Зачем не отдельная таблица:
--   * cardinality маленькая (типичное место — 1–8 тэгов из ~30-словарного
--     перечня), join'ить ради отображения списка избыточно;
--   * никаких агрегаций по тэгам пока не планируется (это приватный кабинет,
--     не публичный feed) — поэтому индекс по тэгам не нужен, GIN отложен
--     до первого case'а «найди все мои места с лисичкой»;
--   * простая схема `TEXT[]` — отражение реальной потребности UI:
--     multi-select chips, сериализуется/десериализуется за один запрос.
--
-- Формат значений: kebab-slug, единый словарь с frontend-side
-- `apps/web/src/lib/spotTags.ts` — там же группировка на «деревья»/«грибы».
-- БД не валидирует список (CHECK constraint обновлять пришлось бы при каждом
-- добавлении вида), это контракт фронта + API.

ALTER TABLE user_spot
    ADD COLUMN IF NOT EXISTS tags TEXT[] NOT NULL DEFAULT '{}';

COMMENT ON COLUMN user_spot.tags IS
    'Тэги места (slug-строки): породы деревьев + виды грибов, '
    'словарь — apps/web/src/lib/spotTags.ts.';
